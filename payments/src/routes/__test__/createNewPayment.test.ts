import mongoose from "mongoose";
import request from "supertest";
import { OrderStatusEnum } from "@ticket-microservice2021/common";
import { app } from "../../app";
import { Order } from "../../models/[payments] orderModel";
import { stripeApiClient } from '../../stripeApiClient';
import { Payment } from "../../models/[payments] paymentModel";

// jest.mock('../../stripe.ts');

